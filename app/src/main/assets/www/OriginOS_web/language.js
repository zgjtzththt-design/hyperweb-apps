function applyLanguage(lang) {
    const t = translations[lang] || translations['en'];
    
    // Set lang attribute but keep LTR direction as requested
    document.documentElement.lang = lang;
    document.documentElement.dir = 'ltr'; 

    // Update common elements by ID
    const phoneName = document.getElementById('phoneName');
    if (phoneName) phoneName.textContent = t.phone_name;

    // Use data-i18n attributes for more complex elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key]) {
            if (key === 'back' || el.classList.contains('back-button')) {
                el.textContent = t[key] + ' ‹';
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update dynamically named things like apps automatically if available
    if (window.customApps) {
        const appKeys = {
            'box1': 'app_calculator',
            'box2': 'app_files',
            'box3': 'app_music',
            'box4': 'app_settings',
            'box5': 'app_messages',
            'box6': 'app_photos',
            'box7': 'app_calendar',
            'box8': 'app_phone',
            'box9': 'app_browser',
            'box10': 'app_weather',
            'box12': 'app_store',
            'box21': 'glass_tuner'
        };
        
        window.customApps.forEach(app => {
            const key = appKeys[app.id];
            if (key && t[key]) {
                app.name = t[key];
            }
        });
        
        // Re-render apps if the function exists
        if (typeof window.renderAllIcons === 'function') {
            window.renderAllIcons();
        }
    }

    // Specific triggers for the welcome screen if it's open
    if (typeof updateTextsLocally === 'function') {
        updateTextsLocally(lang);
    }
    
    // Special case for all.js labels that might be hardcoded in DOM
    const labels = document.querySelectorAll('.label-icon-box p, .icon-box p');
    labels.forEach(label => {
        const text = label.textContent.trim();
        let found = false;
        for (const l in translations) {
            for (const key in translations[l]) {
                if (translations[l][key] === text) {
                    label.textContent = t[key];
                    found = true;
                    break;
                }
            }
            if (found) break;
        }
    });

    // Handle any specific text placeholders or dynamic content in global JS functions
    window.currentLanguage = lang;
    
    if (typeof updatePassLanguage === 'function') {
        updatePassLanguage(lang);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem("language") || 'ar';
    applyLanguage(savedLang);
});
